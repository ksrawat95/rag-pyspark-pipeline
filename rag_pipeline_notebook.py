# DBTITLE 1,Cell 6: Gold Layer Generation & Delta Lake Managed Storage
# Generate dense vector embeddings across our chunked dataset in parallel
gold_df = silver_df.withColumn("vector_embeddings", generate_batch_embeddings_udf(col("chunk_text")))

