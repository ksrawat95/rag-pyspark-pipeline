# DBTITLE 1,Cell 4: Text Chunking Layer (Silver Processing)
# Define a standard Python function to split text into semantic overlapping windows.
# Targets roughly 500 characters with a 50-character overlap.
def chunk_unstructured_text(text: str, chunk_size: int = 500, overlap: int = 50) -> list:
    """
    Splits unstructured text into fixed-size overlapping semantic chunks.
    Returns a list of dictionaries containing chunk indices and character lengths.
    """
    if not text or len(text.strip()) == 0:
        return []
    
    chunks = []
    start = 0
    idx = 0
    text_len = len(text)
    
    while start < text_len:
        # Determine end index
        end = min(start + chunk_size, text_len)
        
        # Pull current window
        chunk = text[start:end]
        
        chunks.append({
            "chunk_index": idx,
            "chunk_text": chunk,
            "char_length": len(chunk)
        })
        
        idx += 1
        
        # Break condition if we've reached the end
        if end == text_len:
            break
            
        # Shift start pointer forward by chunk size minus the desired overlap
        start += (chunk_size - overlap)
        
    return chunks

# Define the PySpark Schema representation for the output chunks array
chunk_struct_schema = ArrayType(StructType([
    StructField("chunk_index", IntegerType(), False),
    StructField("chunk_text", StringType(), False),
    StructField("char_length", IntegerType(), False)
]))

# Wrap the text chunker in a standard PySpark UDF
chunking_udf = udf(lambda t: chunk_unstructured_text(t), chunk_struct_schema)

# Apply chunking and explode the array column to transform documents into row-level semantic chunks
silver_df = bronze_df.withColumn("chunks", chunking_udf(col("text"))) \
    .select("doc_id", "title", "category", explode("chunks").alias("chunk")) \
    .select(
        "doc_id",
        "title",
        "category",
        col("chunk.chunk_index").alias("chunk_index"),
        col("chunk.chunk_text").alias("chunk_text"),
        col("chunk.char_length").alias("char_length")
    )

