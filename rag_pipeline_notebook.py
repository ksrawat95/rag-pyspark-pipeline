# Technical Highlights:
# 1. We leverage a PySpark Vectorized Pandas UDF (Series-to-Series) to process
#    chunks in batches using Apache Arrow memory formats instead of row-by-row.
# 2. To optimize memory usage, the sentence-transformers model is loaded exactly
#    ONCE per executor worker process (lazy-loaded singleton pattern), avoiding
#    exceeding container RAM limitations on free-tier single-node clusters.
# 3. PyTorch utilizes vectorized batch inference automatically when list inputs
#    are passed to the sentence-transformer encode() interface.
#
# ==============================================================================

# Define a global placeholder to implement the lazy-loaded model singleton pattern
_sentence_transformer_model = None

def get_sentence_transformer_model():
    """
    Ensures that the model weights are loaded into CPU memory exactly once
    per worker process session. subsequent evaluations reuse the cached model.
    """
    global _sentence_transformer_model
    if _sentence_transformer_model is None:
        from sentence_transformers import SentenceTransformer
        # Load the lightweight MiniLM model (384-dimensional dense vectors)
        # Highly efficient for Single-Node clusters (Databricks Community Edition)
        _sentence_transformer_model = SentenceTransformer("sentence-transformers/all-MiniLM-L6-v2")
    return _sentence_transformer_model

@pandas_udf(ArrayType(FloatType()))
def generate_batch_embeddings_udf(sentences: pd.Series) -> pd.Series:
    """
    Vectorized PySpark UDF.
    Transforms an Arrow-backed Pandas Series of strings into an Arrow-backed 
    Pandas Series of Float arrays representing dense vector embeddings.
    """
    # 1. Fetch our lazy-loaded model singleton
    model = get_sentence_transformer_model()
    
    # 2. Extract raw text list from the Pandas Series
    text_list = sentences.tolist()
    
    # 3. Generate high-dimensional vector embeddings in a single parallelized pass
    #    (SentenceTransformers executes highly optimized batched PyTorch inference)
    embeddings = model.encode(text_list, show_progress_bar=False)
    
    # 4. Map the resulting 2D NumPy array back into a Pandas Series of native lists
    return pd.Series(list(embeddings))

